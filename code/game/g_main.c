/*
=============
CheckTournament

Once a frame, check for changes in tournement player state
=============
*/
void CheckTournament( void ) {
	// check because we run 3 game frames before calling Connect and/or ClientBegin
	// for clients on a map_restart
	if ( level.numPlayingClients == 0 ) {
		return;
	}

	if ( g_gametype.integer == GT_TOURNAMENT ) {

		// pull in a spectator if needed
		if ( level.numPlayingClients < 2 ) {
			AddTournamentPlayer();
		}

		// if we don't have two players, go back to "waiting for players"
		if ( level.numPlayingClients != 2 ) {
			if ( level.warmupTime != -1 ) {
				level.warmupTime = -1;
				trap_SetConfigstring( CS_WARMUP, va("%i", level.warmupTime) );
				G_LogPrintf( "Warmup:\n" );
			}
			return;
		}

		if ( level.warmupTime == 0 ) {
			return;
		}

		// if the warmup is changed at the console, restart it
		if ( g_warmup.modificationCount != level.warmupModificationCount ) {
			level.warmupModificationCount = g_warmup.modificationCount;
			level.warmupTime = -1;
		}

		// if all players have arrived, start the countdown
		if ( level.warmupTime < 0 ) {
			if ( level.numPlayingClients == 2 ) {
				// fudge by -1 to account for extra delays
				level.warmupTime = level.time + ( g_warmup.integer - 1 ) * 1000;
				trap_SetConfigstring( CS_WARMUP, va("%i", level.warmupTime) );
			}
			return;
		}

		// if the warmup time has counted down, restart
		if ( level.time > level.warmupTime ) {
			level.warmupTime += 10000;
			trap_Cvar_Set( "g_restarted", "1" );
			trap_SendConsoleCommand( EXEC_APPEND, "map_restart 0\n" );
			level.restarted = qtrue;
			return;
		}
	} else if ( g_gametype.integer != GT_SINGLE_PLAYER && level.warmupTime != 0 ) {
		int		counts[TEAM_NUM_TEAMS];
		qboolean	notEnough = qfalse;

		if ( g_gametype.integer > GT_TEAM ) {
			counts[TEAM_BLUE] = TeamCount( -1, TEAM_BLUE );
			counts[TEAM_RED] = TeamCount( -1, TEAM_RED );

			if (counts[TEAM_RED] < 1 || counts[TEAM_BLUE] < 1) {
				notEnough = qtrue;
			}
		} else if ( level.numPlayingClients < 2 ) {
			notEnough = qtrue;
		}

		if ( notEnough ) {
			if ( level.warmupTime != -1 ) {
				level.warmupTime = -1;
				trap_SetConfigstring( CS_WARMUP, va("%i", level.warmupTime) );
				G_LogPrintf( "Warmup:\n" );
			}
			return; // still waiting for team members
		}

		if ( level.warmupTime == 0 ) {
			return;
		}

		// if the warmup is changed at the console, restart it
		if ( g_warmup.modificationCount != level.warmupModificationCount ) {
			level.warmupModificationCount = g_warmup.modificationCount;
			level.warmupTime = -1;
		}

		// if all players have arrived, start the countdown
		if ( level.warmupTime < 0 ) {
			// fudge by -1 to account for extra delays
			level.warmupTime = level.time + ( g_warmup.integer - 1 ) * 1000;
			trap_SetConfigstring( CS_WARMUP, va("%i", level.warmupTime) );
			return;
		}

		// if the warmup time has counted down, restart
		if ( level.time > level.warmupTime ) {
			level.warmupTime += 10000;
			trap_Cvar_Set( "g_restarted", "1" );
			trap_SendConsoleCommand( EXEC_APPEND, "map_restart 0\n" );
			level.restarted = qtrue;
			return;
		}
	}
}


/*
==================
CheckVote
==================
*/
void CheckVote( void )
{
	if( level.voteExecuteTime && level.voteExecuteTime < level.time )
	{
		level.voteExecuteTime = 0;
		trap_SendConsoleCommand( EXEC_INSERT, (level.voteResult)?level.votePassedScript:level.voteFailedScript );
	}

	if( !level.voteTime )
		return;

	gs.COUNTDOWN = g_voteTimeOut.integer - ( level.time - level.voteTime )/1000;

	{
		int voteYes=0;
		int voteNo=0;
		int voteAbstain=0;
		int	voteTotal=0;

		char info[ MAX_INFO_STRING ];
		trap_GetConfigstring( CS_VOTE_STATE, info, sizeof(info) );

		//	vote timed out, anyone who hasn't voted abstains
		if( level.time - level.voteTime >= (g_voteTimeOut.integer*1000) ) {
			sql( "UPDATE players SET vote = ?2 WHERE vote = ?1;", "iie", GS_VOTENONE, GS_VOTEABSTAIN );
		}

		//	tally the votes
		if ( sql( "SELECT COUNT(*),SUM(vote=?1),SUM(vote=?2),SUM(vote=?3) FROM players;", "iii", GS_VOTEABSTAIN, GS_VOTEYES, GS_VOTENO ) )
		{
			voteTotal	= sqlint(0);
			voteAbstain = sqlint(1);
			voteYes		= sqlint(2);
			voteNo		= sqlint(3);
			sqldone();
		}

		// still waiting for everyone to vote
		if ( voteYes + voteNo + voteAbstain < voteTotal )
			return;
	  
		if( voteYes > (voteTotal - voteAbstain) / 2 )
		{
			//
			//	vote passed
			//
			Info_SetValueForKey( info, "t", "passed" );
			trap_SetConfigstring( CS_VOTE_STATE, info );

			//	execute the passed script
			if ( level.votePassedScript[ 0 ] != '\0' )
			{
				level.voteResult = 1;
				level.voteExecuteTime = level.time + level.voteExecuteTime ? (g_voteDelay.integer * 1000) : 0;
			}
			// if we have more then one vote(ex: more then one player)
			if (voteTotal > 1 ) {
				trap_SendServerCommand( -1, "st_announcer vote_passed 1500\n");
			}
				
		}
		else if( voteNo >= (voteTotal - voteAbstain) / 2 )
		{
			//
			//	vote failed
			//
			Info_SetValueForKey( info, "t", "failed" );
			trap_SetConfigstring( CS_VOTE_STATE, info );

			//	execute the failed script
			if ( level.voteFailedScript[ 0 ] != '\0' )
			{
				level.voteResult = 0;
				level.voteExecuteTime = level.time + level.voteExecuteTime ? (g_voteDelay.integer * 1000) : 0;
			}
			level.voteFailedTime = level.time + (g_voteFailedDelay.integer * 1000);
			// if we have more then one vote(ex: more then one player)
			if (voteTotal > 1 ) {
				trap_SendServerCommand( -1, "st_announcer vote_failed 1500\n");
			}

		} else
			// still waiting for a majority
			return;

		
	}

	level.voteTime = 0;

	// notify clients that the vote has finished.
	gs.MENU = 0;
	gs.COUNTDOWN = 0;
}

